# Função para carregar a planilha
def carregar_planilha():
    root = tk.Tk()
    root.withdraw()
    return filedialog.askopenfilename(title="Selecione a planilha XLSX", filetypes=(("Excel Files", "*.xlsx"), ("All Files", "*.*")))

# Função para verificar as probabilidades e pintar as células
def verificar_e_pintar(prob_100, prob_50, ws, cor, nome_cor, max_row):
    # Pré-calcule as células de cada linha
    for row in range(2, max_row + 1):
        p100, p50 = ws[f'D{row}'].value, ws[f'E{row}'].value
        if float(p100) == float(prob_100) and float(p50) == float(prob_50):
            # Pintar as células com a cor
            ws[f'D{row}'].fill = openpyxl.styles.PatternFill(start_color=cor, end_color=cor, fill_type="solid")
            ws[f'E{row}'].fill = openpyxl.styles.PatternFill(start_color=cor, end_color=cor, fill_type="solid")
            ws[f'K{row}'] = nome_cor  # Nome da cor na coluna K
            verificar_acerto(row, ws, cor)  # Verificar acerto/erro

# Função para verificar acerto ou erro nas jogadas abaixo
def verificar_acerto(linha, ws, cor):
    acerto_encontrado = False
    for i in range(1, 3):  # Verifica as duas jogadas abaixo
        linha_jogada = linha + i
        if linha_jogada <= ws.max_row:
            jogada = ws[f'B{linha_jogada}'].value
            if (cor == "000000" and "black" in jogada.lower()) or (cor == "FFFFFF" and "white" in jogada.lower()) or (cor == "FF0000" and "red" in jogada.lower()):
                acerto_encontrado = True
                ws[f'L{linha}'] = 'Acerto Direto' if i == 1 else 'Acerto Gale'
                break
    if not acerto_encontrado:
        ws[f'L{linha}'] = 'Erro'

